var alien = "green";
if (alien == "green") {
    console.log("Congrats! You just earned 5 points for shooting green Alien");
}
else if (alien === "yellow") {
    console.log("Congrats! You just earned 10 points for shooting yellow Alien");
}
else if (alien === "red") {
    console.log("Congrats! You just earned 15 points for shooting red Alien");
}
else {
    console.log("No points, Sorry!");
}
